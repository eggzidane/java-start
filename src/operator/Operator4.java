package operator;

public class Operator4 {
    public static void main(String[] args) {
        int sum3 = 2 * 2 + 3 * 3; // (2 * 2) + (3 * 3)
        int sum4 = (2 * 2) + (3 * 3); //sum3 같다
        System.out.println("sum3 =" + sum3);
        System.out.println("sum4 =" + sum4); //그냥 코드 봤을때 생각해야하면 최대한 단순하게 만든다.
    }
}
