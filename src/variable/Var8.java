package variable;

public class Var8 {
    public static void main(String[] args) {
        //정수
        byte b = 127; // -128 ~ 127
        short s = 32767; // -32768 ~32767
        int i = 2147483647; // -2147483648 ~ 2147483647 (약 20억)

        long l = 922337231232132L; // 실수 리터럴은 기본이 int라서 뒤에 L을붙임

        //실수
        float f = 10f; //실수 리터럴은 기본이 double형이어서 f를 붙임
        double d = 10.0;

    }
}
